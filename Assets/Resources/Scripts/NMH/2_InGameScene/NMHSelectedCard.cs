using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

public class NMHSelectedCard : MonoBehaviour
{
    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    //아래는 private 변수

    NMHCard cSelectedCard;

    Sprite sprForeground;

    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    //아래는 public 변수

    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    //아래는 private 함수

    private void Start()
    {
        initSelectedCard();
    }

    private void initSelectedCard()                                                  //초기화
    {
        sprForeground = new Sprite();
        sprForeground = this.transform.Find("foreground_selected_card").GetComponent<Image>().sprite;
    }

    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////
    //아래는 public 함수

    public Sprite GetForeground()
    {
        return sprForeground;
    }

    public void SetForeground(Sprite _sprTarget)
    {
        sprForeground = _sprTarget;
        this.transform.Find("foreground_selected_card").GetComponent<Image>().sprite = sprForeground;   //타겟 스프라이트를 자식의 이미지에 넣음
    }
}
